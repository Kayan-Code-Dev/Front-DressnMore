export function loadInvoiceRules(): InvoiceRulesState {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    if (raw) return JSON.parse(raw) as InvoiceRulesState;
  } catch {
    /* ignore */
  }
  return defaultRules;
}
