export function filterLedgerEntries(
  entries: LedgerEntry[],
  params: import("@/features/cashboxes/types/statement.types").StatementFilterParams,
): LedgerEntry[] {
  const normalized = (params.search ?? "").trim().toLowerCase();
  return entries.filter((entry) => {
    if (normalized) {
      const haystack = [
        entry.reference,
        entry.description,
        entry.party,
        entry.branch_name,
        entry.category,
      ]
        .join(" ")
        .toLowerCase();
      if (!haystack.includes(normalized)) return false;
    }
    if (params.branch_id && entry.branch_id !== params.branch_id) return false;
    if (params.category && entry.category !== params.category) return false;
    if (params.type === "in" && !entry.credit) return false;
    if (params.type === "out" && !entry.debit) return false;
    if (params.date_from && entry.date < params.date_from) return false;
    if (params.date_to && entry.date > params.date_to) return false;
    return true;
  });
}

export function computeStatementSummary(entries: LedgerEntry[]): StatementSummary {
  const totalRevenues = entries.reduce((sum, e) => sum + (e.credit ?? 0), 0);
  const totalExpenses = entries.reduce((sum, e) => sum + (e.debit ?? 0), 0);
  const lastIncome = [...entries].reverse().find((e) => e.credit)?.date ?? null;
  const lastExpense = [...entries].reverse().find((e) => e.debit)?.date ?? null;
  const currentBalance = OPENING_BALANCE + totalRevenues - totalExpenses;

  return {
    opening_balance: OPENING_BALANCE,
    total_revenues: totalRevenues,
    total_expenses: totalExpenses,
    current_balance: currentBalance,
    available_in_cashbox: 0,
    closing_balance: currentBalance,
    last_income_date: lastIncome,
    last_expense_date: lastExpense,
    entry_count: entries.length,
  };
}
