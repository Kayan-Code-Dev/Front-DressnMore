export function computeCashboxStats(items: CashboxItem[]): CashboxStats {
  const active = items.filter((item) => item.is_active);
  return {
    total_balances: items.reduce((sum, item) => sum + item.current_balance, 0),
    total_revenues: items.reduce((sum, item) => sum + (item.total_in ?? 0), 0),
    total_expenses: items.reduce((sum, item) => sum + (item.total_out ?? 0), 0),
    active_count: active.length,
    total_count: items.length,
  };
}

