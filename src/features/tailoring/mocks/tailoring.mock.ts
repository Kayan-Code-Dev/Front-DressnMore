export function getUrgentOrders(orders: TailoringOrder[]): TailoringOrder[] {
  return orders
    .filter((o) => o.status === "overdue" || o.priority === "urgent" || (o.days_remaining !== undefined && o.days_remaining <= 1))
    .slice(0, 5);
}

export function getReadyOrders(orders: TailoringOrder[]): TailoringOrder[] {
  return orders.filter((o) => o.current_stage === "ready_for_delivery").slice(0, 5);
}
