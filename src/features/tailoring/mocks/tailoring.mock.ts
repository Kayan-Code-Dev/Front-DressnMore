export function computeTailoringStats(orders: TailoringOrder[]): TailoringOrderStats {
  const activeOrders = orders.filter((o) => o.status !== "cancelled");
  const stage_distribution: Partial<Record<TailoringStage, number>> = {};

  for (const stage of TAILORING_STAGES) {
    stage_distribution[stage.key] = activeOrders.filter((o) => o.current_stage === stage.key).length;
  }

  const inProgress = activeOrders.filter(
    (o) => o.status === "active" && !["ready_for_delivery", "delivered"].includes(o.current_stage),
  ).length;

  return {
    total: activeOrders.length,
    active: activeOrders.filter((o) => o.status === "active").length,
    in_progress: inProgress,
    overdue: activeOrders.filter((o) => o.status === "overdue").length,
    ready: activeOrders.filter((o) => o.current_stage === "ready_for_delivery").length,
    completed: activeOrders.filter((o) => o.status === "completed").length,
    vip_count: activeOrders.filter((o) => o.priority === "VIP").length,
    revenue: activeOrders.reduce((s, o) => s + o.total_price, 0),
    collected: activeOrders.reduce((s, o) => s + o.paid, 0),
    remaining: activeOrders.reduce((s, o) => s + o.remaining, 0),
    unpaid_count: activeOrders.filter((o) => o.payment_status === "unpaid").length,
    stage_distribution,
  };
}

