export function computeTailoringStats(orders: TailoringOrder[]): TailoringOrderStats {
  return {
    total: orders.length,
    active: orders.filter((o) => o.status === "active").length,
    overdue: orders.filter((o) => o.status === "overdue").length,
    ready: orders.filter((o) => o.current_stage === "ready_for_delivery").length,
    revenue: orders.reduce((s, o) => s + o.total_price, 0),
  };
}

