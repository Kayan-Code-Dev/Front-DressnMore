export function computeSalesSummary(invoices: SaleInvoice[]): SalesReportSummary {
  const total_sales = invoices.reduce((s, i) => s + i.total, 0);
  const invoices_count = invoices.length;
  return {
    total_sales,
    invoices_count,
    average_invoice_value: invoices_count > 0 ? total_sales / invoices_count : 0,
  };
}

export function computeSaleInvoiceStats(invoices: SaleInvoice[]): SaleInvoiceStats {
  return {
    total: invoices.length,
    completed: invoices.filter((i) => i.invoice_status === "completed").length,
    in_progress: invoices.filter((i) => i.invoice_status === "in_progress").length,
    revenue: invoices.reduce((s, i) => s + i.total, 0),
    collected: invoices.reduce((s, i) => s + (i.paid ?? 0), 0),
    remaining: invoices.reduce((s, i) => s + (i.remaining ?? 0), 0),
  };
}

