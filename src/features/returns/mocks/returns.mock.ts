export function computeInvoiceReturnStats(items: InvoiceReturnItem[]): InvoiceReturnStats {
  const distribution = { waiting: 0, returned: 0, late: 0 };
  let maxDelay = 0;

  for (const item of items) {
    distribution[item.return_status]++;
    maxDelay = Math.max(maxDelay, item.delay_days ?? 0);
  }

  return {
    total: items.length,
    late_returns: distribution.late,
    returned: distribution.returned,
    waiting: distribution.waiting,
    max_delay_days: maxDelay,
    penalties_total: items.reduce((s, i) => s + (i.penalty_amount ?? 0), 0),
    penalties_due: items.reduce((s, i) => s + (i.penalty_due ?? 0), 0),
    penalties_collected: items.reduce((s, i) => s + (i.penalty_paid ?? 0), 0),
    revenue: items.reduce((s, i) => s + i.total_price, 0),
    status_distribution: distribution,
  };
}

