export function computeInvoiceDeliveryStats(items: InvoiceDeliveryItem[]): InvoiceDeliveryStats {
  const distribution = {
    waiting: 0,
    received: 0,
    delivered: 0,
    returned: 0,
    late: 0,
  };

  let todayWeddings = 0;
  const today = new Date().toISOString().slice(0, 10);

  for (const item of items) {
    distribution[item.delivery_status]++;
    if (item.event_date === today) todayWeddings++;
  }

  return {
    total: items.length,
    today_weddings: todayWeddings,
    waiting_delivery: distribution.waiting,
    late_returns: distribution.late,
    revenue: items.reduce((s, i) => s + i.total_price, 0),
    collected: items.reduce((s, i) => s + i.paid, 0),
    remaining: items.reduce((s, i) => s + i.remaining, 0),
    status_distribution: distribution,
  };
}

